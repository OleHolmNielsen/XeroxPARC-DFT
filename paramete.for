      SUBROUTINE PARAM (ITYPE,NATOM,POTPAR,POTTYP,POTXC,POTFIL,
     +                  ULA,NSPIN,NTYPMX,NPARAM)
C
C     ENTER PSEUDOPOTENTIAL PARAMETERS
C
      INTEGER POTTYP(NTYPMX), POTXC(NTYPMX)
      CHARACTER*80 POTFIL(NTYPMX)
      DOUBLE PRECISION POTPAR(NTYPMX,NPARAM)
C
C.....FILES
      COMMON /FILES/INPUT,IOUT,IN290,IN213,ISTORE,IUNIT7,IUNIT8,ISTRUC,
     +               IVNLKK,ISUMRY,IKPTS
C
C.....NUMERICAL AND PHYSICAL CONSTANTS USED THROUGHOUT PROGRAM
      DOUBLE PRECISION ABOHR,RYEV,RYDERG,PI,SPI
      COMMON /CONST/   ABOHR,RYEV,RYDERG,PI,SPI
C
C-----------------------------------------------------------------------
100   WRITE (IUNIT7,110) ITYPE,NATOM
110   FORMAT ('0ATOM TYPE',I3,' WITH ATOMIC NUMBER',I3/
     +' CHOOSE A POTENTIAL FROM THE LIST AND ENTER ITS CODE NUMBER')
      READ (INPUT,*) POTTYP(ITYPE)
      WRITE (IOUT,*) POTTYP(ITYPE)
      IFOUND = 0
C
C     QUESTIONING THE USER ABOUT THE PARAMETERS OF THE CHOSEN POTENTIAL.
C
      IF (POTTYP(ITYPE) .EQ. 10) THEN
C
C.....JELLIUM
C
      WRITE(IUNIT7,*) 'JELLIUM POTENTIAL'
      IF (NSPIN .NE. 1) GOTO 700
      WRITE(IUNIT7,*) 'ENTER THE CHARGE (IN ELECTRONS PER UNIT CELL)'
      READ (INPUT,*) POTPAR(ITYPE,1)
      WRITE(IUNIT7,*) 'JELLIUM CHARGE =',POTPAR(ITYPE,1)
      POTXC(ITYPE) = 0
C
      ELSE IF (POTTYP(ITYPE) .EQ. 11) THEN
C
C.....PURE COULOMB POTENTIAL
C
      WRITE(IUNIT7,*) 'PURE COULOMB POTENTIAL'
C     CALL A SUBROUTINE LOCATING THE PARAMETERS IN A "DATA BASE"
C
      CALL PARM11 (IFOUND,ITYPE,NATOM,POTPAR,NTYPMX,NPARAM)
C
      IF (IFOUND .EQ. 0) THEN
        WRITE (IOUT,*) 'FATAL ERROR *** ATOM NO. ',NATOM,
     +    'IS NOT YET IN DATA BASE'
        CALL EXIT
        ENDIF
      POTXC(ITYPE) = 0
C
      ELSE IF (POTTYP(ITYPE) .EQ. 13) THEN
C
C.....APPELBAUM-HAMANN POTENTIAL, POT13
C
      CALL PARM13 (IFOUND,ITYPE,NATOM,POTPAR,NTYPMX,NPARAM)
C     X-ALPHA CORRELATION:
      POTXC(ITYPE) = 1
      IF (NSPIN .NE. 1) GOTO 700
C
      IF (IFOUND .EQ. 0) THEN
C
        WRITE (IOUT,*) 'APPELBAUM-HAMANN PSEUDOPOTENTIAL :',
     +    ' ENTER PARAMETERS'
        WRITE (IUNIT7,*) '(SEE THE DESCRIPTION OF SBRT. POT13)'
        WRITE (IUNIT7,210)
210     FORMAT (16X,'ZV',11X,'ALPHA',14X,'V1',14X,'V2')
        READ (INPUT,*) ZV,ALPHA,V1,V2
        WRITE (IUNIT7,220) ZV,ALPHA,V1,V2
220     FORMAT (6X,4G16.6)
        POTPAR(ITYPE,1) = ZV
        POTPAR(ITYPE,2) = ALPHA
        POTPAR(ITYPE,3) = V1
        POTPAR(ITYPE,4) = V2
C
        WRITE (IOUT,230)
230     FORMAT('0IMPOSE SOME CUTOFF UPON THIS POTENTIAL?'/
     +    ' (-1.=NO; YES=VALUE OF THE SMALLEST G**2 FOR WHICH',
     +    ' WE WISH V(IONIC)(G).EQ.0 ;'/' (IN ATOMIC UNITS)')
        READ (INPUT,*) G2CUTF
        WRITE (IOUT,220) G2CUTF
        POTPAR(ITYPE,7) = G2CUTF
C       IN UNITS (2PI/ULA)**2
        IF (G2CUTF .GT. 0.0) G2CUTF = G2CUTF * (2.0*PI*ABOHR/ULA)**2
        POTPAR(ITYPE,6) = G2CUTF
C
        ENDIF
C
      ELSE IF (POTTYP(ITYPE) .EQ. 15) THEN
C
C.....BERKELEY IONIC POTENTIAL, POT15:
C
      CALL PARM15 (IFOUND,ITYPE,NATOM,POTPAR,NTYPMX,NPARAM)
C
      IF (POTPAR(ITYPE,7) .GT. 0.0) THEN
C     IN UNITS (2PI/ULA)**2
        POTPAR(ITYPE,6) = POTPAR(ITYPE,7) * (2.0*PI*ABOHR/ULA)
      ELSE
        POTPAR(ITYPE,6) = POTPAR(ITYPE,7)
        ENDIF
C     X-ALPHA CORRELATION:
      POTXC(ITYPE) = 1
      IF (NSPIN .NE. 1) GOTO 700
C
      IF (IFOUND .EQ. 0) THEN
C       ATOM NOT IN DATA BASE, ASK FOR PARAMETERS:
        WRITE (IOUT,*) 'BERKELEY IONIC POTENTIAL: ENTER PARAMETERS'
        WRITE (IUNIT7,*)' (SEE THE DESCRIPTION OF SBRT. POT15)'
        WRITE (IUNIT7,310)
310     FORMAT (14X,'A1',12X,'A2',12X,'A3',12X,'A4',9X,'VNORM')
        READ (INPUT,*) AA1,AA2,AA3,AA4,VNORM
        WRITE (IUNIT7,320) AA1,AA2,AA3,AA4,VNORM
320     FORMAT (2X,5G14.6)
        POTPAR(ITYPE,1) = AA1
        POTPAR(ITYPE,2) = AA2
        POTPAR(ITYPE,3) = AA3
        POTPAR(ITYPE,4) = AA4
        POTPAR(ITYPE,5) = VNORM
        WRITE (IOUT,230)
        READ (INPUT,*)    G2CUTF
        WRITE (IOUT,220)  G2CUTF
        POTPAR(ITYPE,7) = G2CUTF
C       IN UNITS (2PI/ULA)**2
        IF (G2CUTF .GT. 0.0) G2CUTF = G2CUTF * (2.0*PI*ABOHR/ULA)**2
        POTPAR(ITYPE,6)=G2CUTF
        ENDIF
C
      ELSE IF (POTTYP(ITYPE) .EQ. 16) THEN
C
C.....HAMANN-SCHLUTER-CHIANG NON-LOCAL POTENTIAL
C
      WRITE(IUNIT7,*) 'HAMANN-SCHLUTER-CHIANG NON-LOCAL POTENTIAL',
     +  ' (SUBROUTINE POT16)'
      IF (NSPIN .NE. 1) GOTO 700
C
      CALL PARM16(IFOUND,ITYPE,NATOM,POTPAR,NTYPMX,NPARAM)
C
C     WIGNER CORRELATION:
      POTXC(ITYPE) = 2
      IF (IFOUND .EQ. 0) THEN
C       ATOM NOT IN DATA BASE, ASK FOR PARAMETERS:
        WRITE (IOUT,*) 'ENTER PARAMETERS:'
        WRITE (IUNIT7,*) 'THE  L O C A L  PART OF THE POTENTIAL'
        WRITE (IUNIT7,*) 'ENTER CHARGE Z AND ALPHA-0 ?'
        READ  (INPUT,*)  Z,ALPHA0
        WRITE (IUNIT7,*) Z,ALPHA0
        POTPAR(ITYPE,1) = Z
        POTPAR(ITYPE,2) = ALPHA0
C       LOOP OVER ANGULAR MOMENTUM L
        WRITE (IUNIT7,*) 'ENTER THE MAXIMUM L-VALUE (ANGULAR MOMENTUM)'
        READ(INPUT,*) LMAX
        WRITE(IUNIT7,*) LMAX
        POTPAR(ITYPE,3) = FLOAT(LMAX) + 1.0E-6
        IPARAM = 3
        ILMAX = LMAX + 2
        DO 440 IL = 1,ILMAX
          L = IL - 2
          IF (L .LT. 0)
     +      WRITE (IUNIT7,*) 'THE  L O C A L  PART OF THE POTENTIAL'
          IF (L .GE. 0) WRITE(IUNIT7,*) 'ANGULAR MOMENTUM L = ',L
          WRITE (IUNIT7,*) 'HOW MANY GAUSSIANS FOR THIS POTENTIAL ?'
          READ  (INPUT,*)  NGAUS
          WRITE (IUNIT7,*) NGAUS
          IPARAM = IPARAM + 1
          POTPAR(ITYPE,IPARAM) = FLOAT(NGAUS) + 1.0E-6
          IF (NGAUS .LE. 0) GOTO 440
          DO 430 IGAUS = 1,NGAUS
            WRITE(IUNIT7,410) IGAUS
410         FORMAT(' ENTER PREFACTOR CI(L) AND DECAYFACTOR ALPHA-I(L)'/
     +        ' FOR GAUSSIAN NO. ',I3)
            READ (INPUT,*) CI,ALPHAI
            WRITE(IUNIT7,*) CI,ALPHAI
            IPARAM = IPARAM + 1
            POTPAR(ITYPE,IPARAM) = CI
            IPARAM = IPARAM + 1
            POTPAR(ITYPE,IPARAM) = ALPHAI
C           CHECK ARRAY BOUNDS
            IF (IPARAM .LE. NPARAM) GOTO 430
            WRITE(IOUT,420) IPARAM,NPARAM
420         FORMAT(' WE HAVE ACCUMULATED ',I4,' PARAMETERS, EXCEEDING',
     +        ' THE MAXIMUM ',I4/' PROGRAM ABORT')
            CALL EXIT
C
430         CONTINUE
440       CONTINUE
C
        ENDIF
C
      ELSE IF (POTTYP(ITYPE) .EQ. 17) THEN
C
C.....BELL LABS 1982-"PERIODIC TABLE" NON-LOCAL POTENTIAL
C
      WRITE(IUNIT7,*) 'BELL LABS 1982-"PERIODIC TABLE"'
      CALL PARM17 (IFOUND,ITYPE,NATOM,POTPAR,NTYPMX,NPARAM)
C
C     CEPERLEY-ALDER CORRELATION:
      POTXC(ITYPE) = 3
      IF (IFOUND .EQ. 0) THEN
        WRITE (IOUT,*) 'FATAL ERROR *** ATOM NO. ',NATOM,
     +    'IS NOT YET IN DATA BASE'
        CALL EXIT
        ENDIF
C
      ELSE IF (POTTYP(ITYPE) .EQ. 20) THEN
C
C.....OUR OWN NUMERICALLY GENERATED POTENTIALS
C
      CALL PARM20(IFOUND,ITYPE,NATOM,POTXC,POTPAR,POTFIL,NTYPMX,NPARAM)
C
      IF (IFOUND .EQ. 0) THEN
        WRITE (IOUT,*) 'FATAL ERROR *** ATOM NO. ',NATOM,
     +    'IS NOT YET IN DATA BASE'
        CALL EXIT
        ENDIF
C
C.....ILLEGAL CHOICE
C
      ELSE
C
      WRITE (IOUT,130) POTTYP(ITYPE)
130   FORMAT ('0PARAM - WARNING:'/' CODE =',I3,' DOES NOT CORRESPOND ',
     +'TO ANY OF THE AVAILABLE POTENTIALS.'/' MAKE A BETTER CHOICE'/)
      GO TO 100
C
      ENDIF
C
      RETURN
C
700   WRITE (IOUT,710) POTTYP(ITYPE), NSPIN
710   FORMAT ('0PARAM *** POTENTIAL TYPE ',I3,' ILLEGAL FOR NSPIN =',I6/
     +  ' (A SPIN-POLARIZED CALCULATION IS VALID FOR'/' TYPES 11,17,20',
     +  ' WITH CEPERLEY-ALDER CORRELATION)' )
      CALL EXIT
      RETURN
      END
