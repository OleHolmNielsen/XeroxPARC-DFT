      SUBROUTINE CITOAI(PARAM)
C
C     TRANSFORMATION OF THE C1-6 INTO A1-6
C     (FOR BELL LABS NON-LOCAL POTENTIALS)
C
C     PARAM(1..3) CONTAINS THE ALPHA-I'S
C     PARAM(4..6) CONTAINS THE C-I'S ON INPUT, THE A-I'S ON OUTPUT
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C
      DOUBLE PRECISION PARAM(9),C(6),Q(6,7),S(6,6),QINV(6,6)
C
C.....FILES
      COMMON /FILES/INPUT,IOUT,IN290,IN213,ISTORE,IUNIT7,IUNIT8,ISTRUC,
     +               IVNLKK,ISUMRY,IKPTS
C.....PHYSICAL AND MATHEMATICAL CONSTANTS
      DOUBLE PRECISION ABOHR,RYEV,RYDERG,PI,SPI
      COMMON /CONST/   ABOHR,RYEV,RYDERG,PI,SPI
C
C-----------------------------------------------------------------------
C
C     CONSTRUCT THE OVERLAP MATRIX S
C
      DO 140 I = 1,6
      ALFI = PARAM(MOD(I - 1,3) + 1)
      DO 140 L = I ,6
        AA = ALFI + PARAM(MOD(L - 1,3) + 1)
        IF (I .LE. 3) THEN
          IF (L .LE. 3) THEN
C           I,L <= 3
            S(I,L) = 0.25D0*SPI      * AA**(- 1.5D0)
          ELSE
C           I <= 3, L > 3
            S(I,L) = 3.D0/8.D0*SPI   * AA**(- 2.5D0)
            ENDIF
        ELSE
C         I,L > 3
          S(I,L) = 15.D0/16.D0*SPI * AA**(- 3.5D0)
          ENDIF
140     CONTINUE
C
C     CONSTRUCT THE ORTHOGONALITY MATRIX Q
C
      DO 200 I = 1 , 6
      DO 200 L = I , 6
C
        IF (L .EQ. I) THEN
C
          Q(I,L) = S(I,L)
          IF (I .GT. 1) THEN
            DO 150 K = 1,(I-1)
              Q(I,L) = Q(I,L) - Q(K,I)**2
150           CONTINUE
            ENDIF
          IF (Q(I,L) .LT. 0.0D0) GOTO 230
          Q(I,L) = DSQRT(Q(I,L))
C
        ELSE
C
C         L > I
          Q(I,L) = S(I,L)
          IF (I .GT. 1) THEN
            DO 180 K = 1,(I-1)
              Q(I,L) = Q(I,L) - Q(K,I)*Q(K,L)
180           CONTINUE
            ENDIF
          Q(I,L) = Q(I,L)/Q(I,I)
C
          ENDIF
C
200       CONTINUE
C-----------------------------------------------------------------------
C
C     ANALYTICAL INVERSION: P.C.PATTNAIK, G.FLETCHER AND J.L.FRY,
C                           PHYS. REV. B 28, 3564 (1983)
C
      QINV(1,1) = 1.0 / Q(1,1)
      QINV(1,2) = - Q(1,2) / ( Q(1,1)*Q(2,2) )
      QINV(1,3) = ( Q(1,2)*Q(2,3) - Q(1,3)*Q(2,2) ) /
     +            ( Q(1,1)*Q(2,2)*Q(3,3) )
      QINV(1,4) = (- Q(1,2)*Q(2,3)*Q(3,4) + Q(1,2)*Q(2,4)*Q(3,3) +
     +               Q(1,3)*Q(2,2)*Q(3,4) - Q(1,4)*Q(2,2)*Q(3,3)) /
     +              (Q(1,1)*Q(2,2)*Q(3,3)*Q(4,4))
      QINV(1,5) =                  ( Q(1,2)*Q(2,3)*Q(3,4)*Q(4,5) -
     + Q(1,2)*Q(2,3)*Q(3,5)*Q(4,4) - Q(1,2)*Q(2,4)*Q(3,3)*Q(4,5) +
     + Q(1,2)*Q(2,5)*Q(3,3)*Q(4,4) - Q(1,3)*Q(2,2)*Q(3,4)*Q(4,5) +
     + Q(1,3)*Q(2,2)*Q(3,5)*Q(4,4) + Q(1,4)*Q(2,2)*Q(3,3)*Q(4,5) -
     + Q(1,5)*Q(2,2)*Q(3,3)*Q(4,4) ) /
     + ( Q(1,1)*Q(2,2)*Q(3,3)*Q(4,4)*Q(5,5) )
      QINV(1,6) = ( - Q(1,2)*Q(2,3)*Q(3,4)*Q(4,5)*Q(5,6)
     +             +  Q(1,2)*Q(2,3)*Q(3,4)*Q(4,6)*Q(5,5)
     +             +  Q(1,2)*Q(2,3)*Q(3,5)*Q(4,4)*Q(5,6)
     +             -  Q(1,2)*Q(2,3)*Q(3,6)*Q(4,4)*Q(5,5)
     +             +  Q(1,2)*Q(2,4)*Q(3,3)*Q(4,5)*Q(5,6)
     +             -  Q(1,2)*Q(2,4)*Q(3,3)*Q(4,6)*Q(5,5)
     +             -  Q(1,2)*Q(2,5)*Q(3,3)*Q(4,4)*Q(5,6)
     +             +  Q(1,2)*Q(2,6)*Q(3,3)*Q(4,4)*Q(5,5)
     +             +  Q(1,3)*Q(2,2)*Q(3,4)*Q(4,5)*Q(5,6)
     +             -  Q(1,3)*Q(2,2)*Q(3,4)*Q(4,6)*Q(5,5)
     +             -  Q(1,3)*Q(2,2)*Q(3,5)*Q(4,4)*Q(5,6)
     +             +  Q(1,3)*Q(2,2)*Q(3,6)*Q(4,4)*Q(5,5)
     +             -  Q(1,4)*Q(2,2)*Q(3,3)*Q(4,5)*Q(5,6)
     +             +  Q(1,4)*Q(2,2)*Q(3,3)*Q(4,6)*Q(5,5)
     +             +  Q(1,5)*Q(2,2)*Q(3,3)*Q(4,4)*Q(5,6)
     +             -  Q(1,6)*Q(2,2)*Q(3,3)*Q(4,4)*Q(5,5) ) /
     +              ( Q(1,1)*Q(2,2)*Q(3,3)*Q(4,4)*Q(5,5)*Q(6,6) )
      QINV(2,2) = 1.0 / Q(2,2)
      QINV(2,3) = - Q(2,3)/( Q(2,2)*Q(3,3) )
      QINV(2,4) = ( Q(2,3)*Q(3,4) - Q(2,4)*Q(3,3) ) /
     +            ( Q(2,2)*Q(3,3)*Q(4,4) )
      QINV(2,5) = ( - Q(2,3)*Q(3,4)*Q(4,5) + Q(2,3)*Q(3,5)*Q(4,4) +
     +                Q(2,4)*Q(3,3)*Q(4,5) - Q(2,5)*Q(3,3)*Q(4,4) ) /
     +              ( Q(2,2)*Q(3,3)*Q(4,4)*Q(5,5) )
      QINV(2,6) =                  ( Q(2,3)*Q(3,4)*Q(4,5)*Q(5,6) -
     + Q(2,3)*Q(3,4)*Q(4,6)*Q(5,5) - Q(2,3)*Q(3,5)*Q(4,4)*Q(5,6) +
     + Q(2,3)*Q(3,6)*Q(4,4)*Q(5,5) - Q(2,4)*Q(3,3)*Q(4,5)*Q(5,6) +
     + Q(2,4)*Q(3,3)*Q(4,6)*Q(5,5) + Q(2,5)*Q(3,3)*Q(4,4)*Q(5,6) -
     + Q(2,6)*Q(3,3)*Q(4,4)*Q(5,5) ) /
     + ( Q(2,2)*Q(3,3)*Q(4,4)*Q(5,5)*Q(6,6) )
      QINV(3,3) = 1.0 / Q(3,3)
      QINV(3,4) = - Q(3,4) / ( Q(3,3)*Q(4,4) )
      QINV(3,5) = ( Q(3,4)*Q(4,5) - Q(3,5)*Q(4,4) ) /
     +            ( Q(3,3)*Q(4,4)*Q(5,5) )
      QINV(3,6) = ( - Q(3,4)*Q(4,5)*Q(5,6) + Q(3,4)*Q(4,6)*Q(5,5) +
     +                Q(3,5)*Q(4,4)*Q(5,6) - Q(3,6)*Q(4,4)*Q(5,5) ) /
     +              ( Q(3,3)*Q(4,4)*Q(5,5)*Q(6,6) )
      QINV(4,4) = 1.0 / Q(4,4)
      QINV(4,5) = - Q(4,5) / ( Q(4,4)*Q(5,5) )
      QINV(4,6) = ( Q(4,5)*Q(5,6) - Q(4,6)*Q(5,5) ) /
     +            ( Q(4,4)*Q(5,5)*Q(6,6) )
      QINV(5,5) = 1.0 / Q(5,5)
      QINV(5,6) = - Q(5,6) / ( Q(5,5)*Q(6,6) )
      QINV(6,6) = 1.0 / Q(6,6)
C
      DO 300 I = 1,6
        C(I) = 0.0
        DO 300 J = I , 6
C         (NOTE THE MINUS SIGN INTRODUCED)
          C(I) = C(I) - QINV(I,J)*PARAM(3 + J)
300       CONTINUE
      DO 220 I = 1,6
        PARAM(3 + I) = C(I)
220     CONTINUE
      RETURN
C-----------------------------------------------------------------------
230   WRITE (IOUT,240) I,L,Q(I,L)
240   FORMAT(' FATAL ERROR *** Q(',I1,',',I1,') = ',F15.6)
      CALL EXIT
      RETURN
      END
      SUBROUTINE PARM20
     +  (IFOUND,ITYPE,NATOM,POTXC,POTPAR,POTFIL,NTYPMX,NPARAM)
C
C     PSEUDOPOTENTIAL PARAMETER DATA BASE
C.....OUR OWN NUMERICALLY GENERATED POTENTIALS
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DOUBLE PRECISION POTPAR(NTYPMX,NPARAM)
      CHARACTER*80 POTFIL(NTYPMX)
      INTEGER POTXC(NTYPMX)
C
C.....FILES
      COMMON /FILES/INPUT,IOUT,IN290,IN213,ISTORE,IUNIT7,IUNIT8,ISTRUC,
     +               IVNLKK,ISUMRY,IKPTS
C.....Machine types
      COMMON /MACH1/ MACTYP
C
      CHARACTER*80 FILNAM
      PARAMETER (MAXTIT = 20)
      CHARACTER*40 TITLE(MAXTIT)
      CHARACTER    NAME*2, ICORR*2, IREL*3, IFCORE*4, ICALC*2, CDTYP*2
C     For Apollo RECL:
      PARAMETER (NQNLMX = 81)
      DIMENSION R(1000), DUMMY(1)
      DOUBLE PRECISION Z
      DATA NR /1000/
C-----------------------------------------------------------------------
90    IFOUND = 0
      WRITE (IOUT,100) ITYPE, NATOM
100   FORMAT (/'0SUBROUTINE PARM20 - NUMERICALLY GENERATED POTENTIALS'/
     +  ' FOR ATOM TYPE ',I3,' WITH ATOMIC NUMBER ',I4/
     +  ' ENTER THE FILENAME CONTAINING FOURIER TRANSFORMED POTENTIALS')
      READ (INPUT,'(A)') FILNAM
C     STRIP OFF TRAILING BLANKS
      IFIL = LENSTR (FILNAM)
      IF (IFIL .LE. 0) THEN
        WRITE (IOUT,*) 'BAD FILENAME:',FILNAM
        GOTO 90
        ENDIF
      IUNIT = 20 + ITYPE
      WRITE (IOUT,110) FILNAM(1:IFIL),IUNIT
110   FORMAT (' FILE = "',A,'"'/' (ASSOCIATED UNIT =',I3,')' )
      POTFIL(ITYPE) = FILNAM(1:IFIL)
C
C     OPEN THE FILE
C
C     IF (MACTYP .EQ. 11) THEN
C       Apollo: Non-ANSI RECL parameter:
C       OPEN (UNIT = IUNIT, FILE = FILNAM(1:IFIL), STATUS = 'OLD',
C    +    FORM = 'UNFORMATTED', RECL=4*NQNLMX**2)
C     ELSE
        OPEN (UNIT = IUNIT, FILE = FILNAM(1:IFIL), STATUS = 'OLD',
     +    FORM = 'UNFORMATTED')
C       ENDIF
C
C     READ THE HEADING
C
      NTITLE = MAXTIT
C     READ RECORD TYPE 20 (ATOM DEFINITION)
      CALL ATOMRD
     +  (IUNIT,IRECTP,NAME,ICORR,IREL,IFCORE,ZION,NTITLE,TITLE,
     +   NR,A,B,R,NQL,DELQL,NQNL,DELQNL,NUMNL,
     +   ICALC,CDTYP,NO,LO,MODE,DUMMY)
      IF (IRECTP .NE. 20) GOTO 500
C
C     CHECK THE ATOMIC NUMBER
      CALL CHARGE(NAME,Z)
      IF (NATOM .NE. NINT(Z)) THEN
        WRITE (IOUT,115) ITYPE,NATOM, NAME, NINT(Z)
115     FORMAT ('0',79('*')/' *** ERROR *** PARM20 ***'/
     +           ' THE ITYPE =',I4,' , ATOMIC NUMBER =',I4/
     +          ' DOES NOT MATCH FILE OF ',A,' ATOM WITH ATOMIC NUMBER',
     +           I4/'0',79('*')///1X)
        IWARN = 2
        ENDIF
C
C     READ RECORD TYPE 23 (RECIPROCAL SPACE MESH)
      CALL ATOMRD
     +  (IUNIT,IRECTP,NAME,ICORR,IREL,IFCORE,ZION,NTITLE,TITLE,
     +   NR,A,B,R,NQL,DELQL,NQNL,DELQNL,NUMNL,
     +   ICALC,CDTYP,NO,LO,MODE,DUMMY)
      IF (IRECTP .NE. 23) GOTO 500
C
      CLOSE (UNIT = IUNIT)
C
C     SAVE THE NECESSARY INFORMATION IN POTPAR
C
      POTPAR(ITYPE,1)  = ZION
      POTPAR(ITYPE,2)  = IUNIT
      POTPAR(ITYPE,3)  = ITYPE
      POTPAR(ITYPE,4)  = NATOM
C     POTPAR(ITYPE,5)  = ALPHA-NONPOLARIZED (CALCULATED BY POT20)
      POTPAR(ITYPE,6)  = NQL
      POTPAR(ITYPE,7)  = DELQL
      POTPAR(ITYPE,8)  = NQNL
      POTPAR(ITYPE,9)  = DELQNL
C     CORRELATION TYPE:
      IEXCOR = 0
      IF (ICORR .EQ. 'XA' .OR. ICORR .EQ. 'xa') IEXCOR = 1
      IF (ICORR .EQ. 'WI' .OR. ICORR .EQ. 'wi') IEXCOR = 2
      IF (ICORR .EQ. 'CA' .OR. ICORR .EQ. 'ca') IEXCOR = 3
      IF (IEXCOR .EQ. 0) THEN
        WRITE (IOUT,*) 'PARM20: ICORR=',ICORR,' IS UNKNOWN'
        CALL EXIT
        ENDIF
      POTPAR(ITYPE,10) = IEXCOR
      POTXC(ITYPE)     = IEXCOR
C
C     SPIN POLARIZATION:
      IF (IREL .EQ. 'NRL' .OR. IREL .EQ. 'nrl') THEN
        POTPAR(ITYPE,11) = DBLE(1)
        WRITE (IOUT,*) 'POTENTIAL IS NON-SPINPOLARIZED'
      ELSE IF (IREL .EQ. 'SPP' .OR. IREL .EQ. 'spp') THEN
C       SPIN POLARIZED
        POTPAR(ITYPE,11) = DBLE(2)
        WRITE (IOUT,*) 'POTENTIAL IS SPIN-POLARIZED'
      ELSE
        WRITE (IOUT,200) IREL
200     FORMAT ('0PARM20 *** ILLEGAL IREL = ',A)
        CALL EXIT
        ENDIF
C     POTPAR(ITYPE,12)  = ALPHA (SPIN UP)
C     POTPAR(ITYPE,13)  = ALPHA (SPIN DOWN)
C     POTPAR(ITYPE,20 + L) ARE USED IN 'VNLRED' FOR STORING
C                          ANGULAR MOMENTA (L=0,1,...?).
      IFOUND = 1
      RETURN
C
C-----------------------------------------------------------------------
500   WRITE (IOUT,510) IRECTP
510   FORMAT('0SUBROUTINE PARM20 *** ERROR ***'/
     +' UNEXPECTED RECORD-TYPE =',I10)
      IWARN = 2
      CALL EXIT
      RETURN
      END
