      SUBROUTINE HTRIBK (NM,N,AR,AI,TAU,M,ZR,ZI)
C     IMPLICIT DOUBLE PRECISION(A-H,O-Z)
C
      INTEGER NM,N
      REAL AR(1),AI(1),TAU(2,1),ZR(NM,1),ZI(NM,1)
      REAL H,S,SI,ZERO
CDIR$ INT24 I,J,K,L,M,II,IROWI,IK
C
C     THIS SUBROUTINE IS A TRANSLATION OF A COMPLEX ANALOGUE OF
C     THE ALGOL PROCEDURE TRBAK1, NUM. MATH. 11, 181-195(1968)
C     BY MARTIN, REINSCH, AND WILKINSON.
C     HANDBOOK FOR AUTO. COMP., VOL.II-LINEAR ALGEBRA, 212-226(1971).
C
C     THIS SUBROUTINE FORMS THE EIGENVECTORS OF A COMPLEX HERMITIAN
C     MATRIX BY BACK TRANSFORMING THOSE OF THE CORRESPONDING
C     REAL SYMMETRIC TRIDIAGONAL MATRIX DETERMINED BY  HTRIDI.
C
C     ON INPUT-
C
C        N IS THE ORDER OF THE MATRIX,
C
C        IROW CONTAINS THE INDEX OF THE FIRST ELEMENT IN ROW I,
C
C        AR AND AI CONTAIN INFORMATION ABOUT THE UNITARY TRANS-
C          FORMATIONS USED IN THE REDUCTION BY  HTRIDI  IN THEIR
C          FULL LOWER TRIANGLES EXCEPT FOR THE DIAGONAL OF AR,
C          THEY ARE STORED IN PACKED FORM ? A11,A12,A22,A31...
C
C        TAU CONTAINS FURTHER INFORMATION ABOUT THE TRANSFORMATIONS,
C
C        M IS THE NUMBER OF EIGENVECTORS TO BE BACK TRANSFORMED,
C
C        ZR CONTAINS THE EIGENVECTORS TO BE BACK TRANSFORMED
C          IN ITS FIRST M*N POSITIONS.
C
C     ON OUTPUT-
C
C        ZR AND ZI CONTAIN THE REAL AND IMAGINARY PARTS,
C          RESPECTIVELY, OF THE TRANSFORMED EIGENVECTORS
C          IN THEIR FIRST M*N POSITIONS.
C
C     NOTE THAT THE LAST COMPONENT OF EACH RETURNED VECTOR
C     IS REAL AND THAT VECTOR EUCLIDEAN NORMS ARE PRESERVED.
C
C     QUESTIONS AND COMMENTS SHOULD BE DIRECTED TO B. S. GARBOW,
C     APPLIED MATHEMATICS DIVISION, ARGONNE NATIONAL LABORATORY
C
C     ------------------------------------------------------------------
C
      SAVE ZERO
      DATA ZERO /0.0D0/
C
C     STATEMENT FUNCTION DEFINING THE CORRESPONDENCE BETWEEN QUADRATIC
C     AND PACKED STORAGE OF MATRICES AR,AI. I >= J IS REQUIRED.
      LOC(I,J) = (I*I - I)/2 + J
C     ------------------------------------------------------------------
C
      IF (M .EQ. 0) GO TO 200
C     ********** TRANSFORM THE EIGENVECTORS OF THE REAL SYMMETRIC
C                TRIDIAGONAL MATRIX TO THOSE OF THE HERMITIAN
C                TRIDIAGONAL MATRIX. **********
      DO 50 K = 1, N
C     ********** LOOP 40 ADDED FOR CRAY VECTORIZATION **********
         DO 40 J = 1, M
            ZI(K,J) = - ZR(K,J) * TAU(2,K)
   40    CONTINUE
C
         DO 50 J = 1, M
            ZR(K,J) = ZR(K,J) * TAU(1,K)
   50 CONTINUE
C
      IF (N .EQ. 1) GO TO 200
C     ********** RECOVER AND APPLY THE HOUSEHOLDER MATRICES **********
      DO 140 I = 2, N
         L = I - 1
         II = LOC(I,I)
         H = AI(II)
         IF (H .EQ. ZERO) GO TO 140
         IROWI = LOC(I,0)
C
         DO 130 J = 1, M
            S  = ZERO
            SI = ZERO
C
            DO 110 K = 1, L
               IK = IROWI + K
               S  = S  + AR(IK) * ZR(K,J) - AI(IK) * ZI(K,J)
               SI = SI + AR(IK) * ZI(K,J) + AI(IK) * ZR(K,J)
  110       CONTINUE
C     ********** DOUBLE DIVISIONS AVOID POSSIBLE UNDERFLOW **********
            S = (S / H) / H
            SI = (SI / H) / H
C
            DO 120 K = 1, L
               IK = IROWI + K
               ZR(K,J) = ZR(K,J) - S * AR(IK) - SI * AI(IK)
               ZI(K,J) = ZI(K,J) - SI * AR(IK) + S * AI(IK)
  120       CONTINUE
C
  130    CONTINUE
C
  140 CONTINUE
C
  200 RETURN
C     ********** LAST CARD OF HTRIBK **********
      END
      SUBROUTINE TRED1(N,A,D,E,E2,RV5)
      INTEGER N
      INTEGER I,J,K,L,IROWI,IROWJ,II,JJ,IK,IL,JK
      REAL A(*),D(*),E(*),E2(*),RV5(*)
      REAL F,G,H,SCALE,ONE,ZERO
CDIR$ INT24 I,J,K,L,IROWI,IROWJ,II,JJ,IK,IL,JK
C
C     THIS SUBROUTINE IS A TRANSLATION OF THE ALGOL PROCEDURE TRED1,
C     NUM. MATH. 11, 181-195(1968) BY MARTIN, REINSCH, AND WILKINSON.
C     HANDBOOK FOR AUTO. COMP., VOL.II-LINEAR ALGEBRA, 212-226(1971).
C
C     THIS SUBROUTINE REDUCES A REAL SYMMETRIC MATRIX
C     TO A SYMMETRIC TRIDIAGONAL MATRIX USING
C     ORTHOGONAL SIMILARITY TRANSFORMATIONS.
C
C     ON INPUT-
C
C        N IS THE ORDER OF THE MATRIX,
C
C        IROW CONTAINS THE INDEX OF THE FIRST ELEMENT IN ROW I,
C
C        A CONTAINS THE REAL SYMMETRIC INPUT MATRIX.  ONLY THE
C          LOWER TRIANGLE OF THE MATRIX NEED BE SUPPLIED.
C          IT IS STORED IN PACKED FORM ? A11,A12,A22,A31...
C
C        RV5 IS A TEMPORARY STORAGE ARRAY
C
C     ON OUTPUT-
C
C        A CONTAINS INFORMATION ABOUT THE ORTHOGONAL TRANS-
C          FORMATIONS USED IN THE REDUCTION IN ITS STRICT LOWER
C          TRIANGLE.  THE FULL UPPER TRIANGLE OF A IS UNALTERED,
C
C        D CONTAINS THE DIAGONAL ELEMENTS OF THE TRIDIAGONAL MATRIX,
C
C        E CONTAINS THE SUBDIAGONAL ELEMENTS OF THE TRIDIAGONAL
C          MATRIX IN ITS LAST N-1 POSITIONS.  E(1) IS SET TO ZERO,
C
C        E2 CONTAINS THE SQUARES OF THE CORRESPONDING ELEMENTS OF E.
C          E2 MAY COINCIDE WITH E IF THE SQUARES ARE NOT NEEDED.
C
C     QUESTIONS AND COMMENTS SHOULD BE DIRECTED TO B. S. GARBOW,
C     APPLIED MATHEMATICS DIVISION, ARGONNE NATIONAL LABORATORY
C
C     ------------------------------------------------------------------
C
      SAVE ONE,ZERO
      DATA ONE,ZERO /1.0D0, 0.0D0/
C
C     STATEMENT FUNCTION DEFINING THE CORRESPONDENCE BETWEEN QUADRATIC
C     AND PACKED STORAGE OF MATRICES AR,AI. I >= J IS REQUIRED.
      LOC(I,J) = (I*I - I)/2 + J
C     ------------------------------------------------------------------
C
      DO 100 I = 1, N
      II = LOC(I,I)
  100 D(I) = A(II)
      DO 300 I = N, 1, -1
         L = I - 1
         H = ZERO
         SCALE = ZERO
         IROWI = LOC(I,0)
         IF (L .LT. 1) GO TO 130
C     ********** SCALE ROW (ALGOL TOL THEN NOT NEEDED) **********
         DO 120 K = 1, L
         IK = IROWI + K
         SCALE = SCALE + ABS(A(IK))
120      CONTINUE
C
         IF (SCALE .NE. ZERO) GO TO 140
  130    E(I) = ZERO
         E2(I) = ZERO
         GO TO 290
C      ********** LOOP 145 ADDED FOR CRAY VECTORIZATION **********
  140    SCALE1 = ONE / SCALE
         DO 145 K = 1, L
            IK = IROWI + K
            A(IK) = A(IK) * SCALE1
  145    CONTINUE
         DO 150 K = 1, L
            IK = IROWI + K
            H = H + A(IK) * A(IK)
  150    CONTINUE
C
         E2(I) = SCALE * SCALE * H
         IL = IROWI + L
         F = A(IL)
         G = -SIGN(SQRT(H),F)
         E(I) = SCALE * G
         H = H - F * G
         A(IL) = F - G
         IF (L .EQ. 1) GO TO 270
C
         DO 180 J = 1, L
            E(J) = ZERO
  180    CONTINUE
C
         DO 240 J = 1, L
C     ********** FORM ELEMENT OF A*U **********
            IROWJ = LOC(J,0)
            JJ = IROWJ + J
            IJ = IROWI + J
            EJ = A(JJ) * A(IJ)
            AIJ = A(IJ)
            DO 190 K = 1, J - 1
               JK = IROWJ + K
               IK = IROWI + K
               EJ     = EJ   + A(JK) * A(IK)
               E(K)   = E(K) + A(JK) * AIJ
  190       CONTINUE
            E(J) = EJ
  240    CONTINUE
C
C     ********** FORM ELEMENT OF P **********
         H1 = ONE / H
         DO 245 J = 1, L
            E(J) = E(J) * H1
  245    CONTINUE
         F = ZERO
         DO 250 J = 1, L
            IJ = IROWI + J
            F = F + E(J) * A(IJ)
  250    CONTINUE
C
         H = F / (H + H)
C     ********** FORM REDUCED A **********
         DO 260 J = 1, L
            IJ = IROWI + J
            F = A(IJ)
            G = E(J) - H * F
            E(J) = G
            IROWJ = LOC(J,0)
C
C      ********** J,K < I   --  CAN VECTORIZE **********
CDIR$       IVDEP
            DO 260 K = 1, J
               JK = IROWJ + K
               IK = IROWI + K
               A(JK) = A(JK) - F * E(K) - G * A(IK)
  260    CONTINUE
C
  270    DO 280 K = 1, L
         IK = IROWI + K
  280    A(IK) = SCALE * A(IK)
C
  290    H = D(I)
         II = IROWI + I
         D(I) = A(II)
         A(II) = H
  300 CONTINUE
C
      RETURN
C     ********** LAST CARD OF TRED1 **********
      END
      SUBROUTINE TRBAK1(NM,N,A,E,M,Z)
      INTEGER NM,N
      REAL A(1),E(1),Z(NM,1)
      REAL S,ONE,ZERO
CDIR$ INT24 I,J,K,L,M,IROWI,IL,IK
C
C     THIS SUBROUTINE IS A TRANSLATION OF THE ALGOL PROCEDURE TRBAK1,
C     NUM. MATH. 11, 181-195(1968) BY MARTIN, REINSCH, AND WILKINSON.
C     HANDBOOK FOR AUTO. COMP., VOL.II-LINEAR ALGEBRA, 212-226(1971).
C
C     THIS SUBROUTINE FORMS THE EIGENVECTORS OF A REAL SYMMETRIC
C     MATRIX BY BACK TRANSFORMING THOSE OF THE CORRESPONDING
C     SYMMETRIC TRIDIAGONAL MATRIX DETERMINED BY  TRED1.
C
C     ON INPUT-
C
C        N IS THE ORDER OF THE MATRIX,
C
C        IROW CONTAINS THE INDEX OF THE FIRST ELEMENT IN ROW I,
C
C        A CONTAINS INFORMATION ABOUT THE ORTHOGONAL TRANS-
C          FORMATIONS USED IN THE REDUCTION BY  TRED1
C          IN ITS STRICT LOWER TRIANGLE,
C          IT IS STORED IN PACKED FORM ? A11,A12,A22,A31...
C
C        E CONTAINS THE SUBDIAGONAL ELEMENTS OF THE TRIDIAGONAL
C          MATRIX IN ITS LAST N-1 POSITIONS.  E(1) IS ARBITRARY,
C
C        M IS THE NUMBER OF EIGENVECTORS TO BE BACK TRANSFORMED,
C
C        Z CONTAINS THE EIGENVECTORS TO BE BACK TRANSFORMED
C          IN ITS FIRST M*N POSITIONS.
C
C     ON OUTPUT-
C
C        Z CONTAINS THE TRANSFORMED EIGENVECTORS
C          IN ITS FIRST M*N POSITIONS.
C
C     NOTE THAT TRBAK1 PRESERVES VECTOR EUCLIDEAN NORMS.
C
C     QUESTIONS AND COMMENTS SHOULD BE DIRECTED TO B. S. GARBOW,
C     APPLIED MATHEMATICS DIVISION, ARGONNE NATIONAL LABORATORY
C
C     ------------------------------------------------------------------
C
      SAVE ONE,ZERO
      DATA ONE,ZERO /1.0D0, 0.0D0/
C
C     STATEMENT FUNCTION DEFINING THE CORRESPONDENCE BETWEEN QUADRATIC
C     AND PACKED STORAGE OF MATRICES AR,AI. I >= J IS REQUIRED.
      LOC(I,J) = (I*I - I)/2 + J
C     ------------------------------------------------------------------
C
      IF (M .EQ. 0) GO TO 200
      IF (N .EQ. 1) GO TO 200
C
      DO 140 I = 2, N
         L = I - 1
         IROWI = LOC(I,0)
         IL = IROWI + L
         IF (E(I) .EQ. ZERO) GO TO 140
C
         DO 130 J = 1, M
            S = ZERO
C
            DO 110 K = 1, L
            IK = IROWI + K
  110       S = S + A(IK) * Z(K,J)
C     ********** DIVISOR BELOW IS NEGATIVE OF H FORMED IN TRED1.
C                DOUBLE DIVISION AVOIDS POSSIBLE UNDERFLOW **********
            S = (S / A(IL)) / E(I)
C
            DO 120 K = 1, L
            IK = IROWI + K
  120       Z(K,J) = Z(K,J) + S * A(IK)
C
  130    CONTINUE
C
  140 CONTINUE
C
  200 RETURN
C     ********** LAST CARD OF TRBAK1 **********
      END
