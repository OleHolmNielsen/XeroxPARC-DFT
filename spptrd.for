      SUBROUTINE SPPTRD (TEXT,IHG,IHC,ISY,LI,NC,IB,V,F0,R,
     +NAT,NDIM9,NDIM10,NUMKPT,IQ1,IQ2,IQ3,WVK0,LWGHT,WVLIST)
C
C     READ A SET OF SPECIAL POINTS FROM FILE IN290
C
      CHARACTER*80 TEXT
      INTEGER F0(48,NDIM9),IB(48),LWGHT(NDIM10)
      REAL V(3,48),R(49,3,3),WVK0(3),WVLIST(3,NDIM10)
      REAL A1(3),A2(3),A3(3),B1(3),B2(3),B3(3),XKAPA(3)
      CHARACTER*80 TEXT1
C
C.....FILES
      COMMON /FILES/INPUT,IOUT,IN290,IN213,ISTORE,IUNIT7,IUNIT8,ISTRUC,
     +               IVNLKK,ISUMRY,IKPTS
C.....COMMON BLOCK FOR KEEPING TRACK OF POSSIBLE WARNINGS
      COMMON /WARN/ IWARN
C     MACHINE TYPE
      COMMON /MACH1/ MACTYP
      COMMON /MACH2/ MACHIN(0:20)
      CHARACTER*7  MACHIN
      DATA INIT /0/
C-----------------------------------------------------------------------
      IF (INIT .NE. 0) GOTO 2300
      INIT = 1
C
C     READING FROM FILE WITH GROUP THEORY AND SPECIAL POINTS
C     (FIRST CALL: READ SYMMETRY INFORMATION)
C
      NUMKPT = 0
      READ (IN290,*,ERR=300,END=300)
     + NPRGR,IDAY,MONTH,IYEAR,IVERSN,MACTYP
      WRITE (IOUT,100) NPRGR,IVERSN,MACHIN(MACTYP)
100   FORMAT(' Reading special points generated by K',I3,
     +  ' (ver.',I3,') on ',A,' computer')
      CALL DAYPRT (IOUT,IYEAR,MONTH,IDAY)
C
      READ (IN290,'(A)') TEXT1
C     CHECK THAT RELEVEANT PART OF TEXT1 IS THE SAME AS TEXT
      DO 120 J = 1, LENSTR(TEXT1)
        IF (TEXT1(J:J) .NE. TEXT(J:J)) THEN
          WRITE (IOUT,*) 'SPPTRD *** WARNING ***'
          WRITE (IOUT,320) TEXT1, TEXT
320       FORMAT (' heading in the special points file:'/1X,A/
     +    ' is not identical with that in V(G)-file:'/1X,A)
          IWARN = 1
          GOTO 130
          ENDIF
120     CONTINUE
C
130   READ (IN290,*,ERR=300,END=300) IRECTP
      READ (IN290,*) NAT1
      READ (IN290,*) A1,A2,A3,B1,B2,B3
      DO 240 K=1,NAT1
C       DUMMY INPUT:
        READ (IN290,*) ITYPE,XKAPA
240     CONTINUE
C WE DO NOT CHECK WHETHER ALL THIS INFORMATION AGREES WITH
C THE ONE READ PREVIOUSLY, BECAUSE MOST OF IT (NAT1,XKAPA,TY)
C CAN VERY WELL BE DIFFERENT IF SOME "GHOST-ATOMS" WERE
C INTRODUCED. IF SO, WE ISSUE A REMINDER:
      IF (NAT .NE. NAT1) WRITE (IOUT,250) NAT - NAT1
250   FORMAT ('0SUBROUTINE SPPTRD - REMINDER: THE LAST ',I3,
     +' SITES IN THE ABOVE LISTS'/' ARE OCCUPIED BY GHOST ATOMS')
C     GROUP-THEORETICAL INFORMATION
      READ (IN290,*) IHG,IHC,ISY,LI,NC,(IB(J),J=1,48)
C     FORMAT (1X,5I5/(12I3))
      READ (IN290,*) ((V(J,I),J=1,3),I=1,48)
      READ (IN290,*) ((F0(I,J),J=1,NAT),I=1,48)
      READ (IN290,*) (((R(K,I,J),J=1,3),I=1,3),K=1,49)
C-----------------------------------------------------------------------
C
C     READING ONE SET OF SPECIAL POINTS
C
2300  READ (IN290,*,ERR=300,END=300) IRECTP
C     CHECK THAT WE REALLY HAVE A RECORD WITH SPECIAL POINTS:
      IF (IRECTP .NE. 2) GOTO 2320
      READ (IN290,*) N
      IF (N .GT. NDIM10) GOTO 2340
      NUMKPT = N
      READ (IN290,*) IQ1,IQ2,IQ3,WVK0
      DO 2350 I=1,NUMKPT
        READ (IN290,*) LWGHT(I),(WVLIST(J,I),J=1,3),(IDUMMY,J=1,48)
2350    CONTINUE
      WRITE (IOUT,2360) NUMKPT,IQ1,IQ2,IQ3,WVK0
2360  FORMAT(I4,' SPECIAL POINTS READ, IQ1,2,3 =',3I3,'; WVK0 =',3F8.3)
C
      RETURN
C-----------------------------------------------------------------------
C
C     ERROR MESSAGES
C
300   WRITE (IOUT,*) 'SPPTRD *** WARNING ***'
      WRITE (IOUT,*) 'ERROR reading special points file, UNIT=',IN290
      WRITE (IOUT,*) 'File closed - execution continues'
      CLOSE (UNIT = IN290 , STATUS = 'KEEP')
      IWARN = 1
      RETURN
C
2320  WRITE (IOUT,*) 'SUBROUTINE SPPTRD - WARNING:'
      WRITE (IOUT,2310) IRECTP
2310  FORMAT (' RECORD WE ARE ATTEMPTING TO READ FROM THE SPECIAL-POINTS
     + FILE'/' IS NOT A RECORD WITH SPECIAL POINTS BUT DATAOF',
     +' RECORD-TYPE  IRECTP =',I3/
     +' K207 CONTINUES WITH THE PREVIOUS SET OF SPECIAL POINTS')
      IWARN = 2
      RETURN
C
2340  WRITE (IOUT,*) 'SUBROUTINE SPPTRD - WARNING:'
      WRITE (IOUT,2330) N,NDIM10
2330  FORMAT (1X,I4,' SPECIAL POINTS IN THIS RECORD CANNOT BE ',
     +'ACCOMMODATED'/' INTO THE LIST WHICH ONLY WAS DIMENSIONED',
     +I4,' IN K207'/
     +' K207 CONTINUES WITH THE PREVIOUS SET OF SPECIAL POINTS')
      IWARN = 2
      RETURN
C
      END
