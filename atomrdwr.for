      SUBROUTINE ATOMRD
     +  (IFILE,IRECTP,NAME,ICORR,IREL,IFCORE,ZION,NTITLE,TITLE,
     +   NR,A,B,R,NQL,DELQL,NQNL,DELQNL,NUMNL,
     +   ICALC,ITYPE,NO,LO,MODE,ARRAY)
C
C     SUBROUTINE TO READ ANY FILE GENERATED BY THE SET OF
C     ATOMIC PROGRAMS.
C     CONVENTION AGREED UPON BY S. FROYEN AND O.H. NIELSEN (6/84)
C
C     IFILE ....... FILE UNIT NUMBER
C     IRECTP ...... RECORD-TYPE DEFINED AS:
C            20 ... DEFINITION OF THE ATOMIC CALCULATION
C            22 ... RADIAL MESH
C            23 ... FOURIER-SPACE MESH
C            31 ... POTENTIAL
C            32 ... VALENCE CHARGE DENSITY
C            33 ... CORE CHARGE DENSITY
C            34 ... WAVEFUNCTION
C            36 ... NONLOCAL MATRIX ELEMENTS
C     NAME ........ ATOMIC NAME
C     ICORR ....... CORRELATION TYPE
C     IREL ........ RELATIVISTIC/NON-DO./SPIN-POLARIZED 'REL',' ','SPP'
C     IFCORE ...... TYPE OF CORE X-C
C     ZION ........ IONIC CHARGE
C     TITLE ....... TEXT ON FILE (NTITLE LINES)
C     NTITLE ...... DIMENSION OF TITLE ON INPUT, NO. OF LINES ON OUTPUT
C     NR,A,B ...... RADIAL GRID
C     NQL,DELQL ... Q-SPACE GRID
C     NQNL,DELQNL . Q-SPACE GRID FOR NONLOCAL POTENTIALS
C     NUMNL ....... NONLOCAL RECORDS (NOT USED)
C     DESCRIPTOR OF ARRAYS:
C     ICALC ....... 'AE' OR 'PS'
C     ITYPE ....... '  ', 'UP', 'DN' OR 'DF'
C     NO .......... PRINCIPAL QUANTUM NUMBER
C     LO .......... ANGULAR MOMENTUM
C     MODE ........ SWITCH FOR FUTURE USE
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      CHARACTER NAME*2, ICORR*2, IREL*3, IFCORE*4, ICALC*2, ITYPE*2
      CHARACTER*40 TITLE(NTITLE)
      DIMENSION R(*),ARRAY(*)
      LOGICAL REALSP, FOURSP
      SAVE    REALSP, FOURSP
C
C.....FILES
      COMMON /FILES/INPUT,IOUT,IN290,IN213,ISTORE,IUNIT7,IUNIT8,ISTRUC,
     +               IVNLKK,ISUMRY,IKPTS
C
      DATA REALSP, FOURSP /2 * .FALSE./
C-----------------------------------------------------------------------
C
C     READ THE RECORD-TYPE
C
      READ (IFILE,END=1100,ERR=1200) IRECTP
      WRITE (IUNIT8,*) 'ATOMRD: Reading record type ', IRECTP
C
      IF (IRECTP .EQ. 20) THEN
C
        READ (IFILE,ERR=1200) NAME,ICORR,IREL,IFCORE,ZION
        WRITE (IUNIT7,100) IFILE,NAME,ICORR,IREL,IFCORE,ZION
100     FORMAT('0Reading unit=',I3,' with ',A,' atom'/
     +    ' corr=',A,' rel=',A,' core=',A,' ionic charge=',F15.8)
        READ (IFILE,ERR=1200) NT
        DO 120 I = 1, NT
          IT = I
          IF (IT .GT. NTITLE) IT = NTITLE
          READ (IFILE,ERR=1200) TITLE(IT)
          WRITE (IUNIT7,110) TITLE(IT)
110       FORMAT(1X,A)
120       CONTINUE
        IF (NT .GT. NTITLE) WRITE (IOUT,130) NT,NTITLE
130     FORMAT(1X,I3,' titles exceed NTITLE=',I3)
        NTITLE = NT
C
      ELSE IF (IRECTP .EQ. 22) THEN
C
        READ (IFILE,ERR=1200) NARRAY,A,B
        WRITE (IUNIT8,200) NARRAY,A,B
200     FORMAT(' Radial mesh: NR=',I6,' A,B=',2F10.3)
        IF (NARRAY .GT. NR) THEN
          WRITE (IOUT,*) 'NR exceeds array dimension =', NR
          CALL EXIT
        ELSE
          NR = NARRAY
          ENDIF
        REALSP = .TRUE.
        FOURSP = .FALSE.
        READ (IFILE,ERR=1200) (R(I), I = 1, NR)
C
      ELSE IF (IRECTP .EQ. 23) THEN
C
        READ (IFILE,ERR=1200) NQL,DELQL,NQNL,DELQNL
        WRITE (IUNIT8,300) NQL,NQNL,DELQL,DELQNL
300     FORMAT(' Fourier-space mesh: NQL,NQNL=',2I6,' DELQL,DELQNL=',
     +    2F10.3)
        FOURSP = .TRUE.
        REALSP = .FALSE.
C
      ELSE IF (IRECTP .GE. 31 .AND. IRECTP .LE. 34) THEN
C
        NARRAY = 0
        IF (FOURSP) NARRAY = NQL
        IF (REALSP) NARRAY = NR
        READ (IFILE,ERR=1200) ICALC,ITYPE,NO,LO,MODE
        READ (IFILE,ERR=1200) (ARRAY(I), I = 1, NARRAY)
C
      ELSE IF (IRECTP .EQ. 36) THEN
C
        READ (IFILE,ERR=1200) ICALC,ITYPE,NO,LO,MODE
C
      ELSE IF (IRECTP .EQ. 0) THEN
C
        WRITE (IOUT,*) 'ATOMRD: end-of-file. IRECTP=',IRECTP
C
      ELSE
C
        WRITE (IOUT,1000) IRECTP
1000    FORMAT('0Subroutine ATOMRD *** WARNING ***'/
     +    ' illegal record-type =',I10)
C
        ENDIF
C
      RETURN
C
1100  WRITE (IOUT,1110) IFILE
1110  FORMAT(' Subroutine ATOMRD - end-of-file unit=',I4)
      IRECTP = 0
      RETURN
C
1200  WRITE (IOUT,1210) IFILE
1210  FORMAT(' Subroutine ATOMRD - error on file unit=',I4)
      IRECTP = 0
      CALL EXIT
      RETURN
      END
      SUBROUTINE ATOMWR
     +  (IFILE,IRECTP,NAME,ICORR,IREL,IFCORE,ZION,NTITLE,TITLE,
     +   NR,A,B,R,NQL,DELQL,NQNL,DELQNL,NUMNL,
     +   ICALC,ITYPE,NO,LO,MODE,ARRAY)
C
C     SUBROUTINE TO WRITE ANY FILE FROM AN ATOMIC CALCULATION.
C     CONVENTION AGREED UPON BY S. FROYEN AND O.H. NIELSEN (6/84)
C
C     IFILE ....... FILE UNIT NUMBER
C     IRECTP ...... RECORD-TYPE DEFINED AS:
C            20 ... DEFINITION OF THE ATOMIC CALCULATION
C            22 ... RADIAL MESH
C            23 ... FOURIER-SPACE MESH
C            31 ... POTENTIAL
C            32 ... VALENCE CHARGE DENSITY
C            33 ... CORE CHARGE DENSITY
C            34 ... WAVEFUNCTION
C            36 ... NONLOCAL MATRIX ELEMENTS
C     NAME ........ ATOMIC NAME
C     ICORR ....... CORRELATION TYPE
C     IREL ........ RELATIVISTIC/NON-DO./SPIN-POLARIZED 'REL',' ','SPP'
C     IFCORE ...... TYPE OF CORE X-C
C     ZION ........ IONIC CHARGE
C     TITLE ....... TEXT ON FILE (NTITLE LINES)
C     NTITLE ...... DIMENSION OF TITLE ON INPUT, NO. OF LINES ON OUTPUT
C     NR,A,B ...... RADIAL GRID
C     NQL,DELQL ... Q-SPACE GRID
C     NQNL,DELQNL . Q-SPACE GRID FOR NONLOCAL POTENTIALS
C     NUMNL ....... NONLOCAL RECORDS (NOT USED)
C     DESCRIPTOR OF ARRAYS:
C     ICALC ....... 'AE' OR 'PS'
C     ITYPE ....... '  ', 'UP', 'DN' OR 'DF'
C     NO .......... PRINCIPAL QUANTUM NUMBER
C     LO .......... ANGULAR MOMENTUM
C     MODE ........ SWITCH FOR FUTURE USE
C
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      CHARACTER NAME*2, ICORR*2, IREL*3, IFCORE*4, ICALC*2, ITYPE*2
      CHARACTER*40 TITLE(*)
      DIMENSION R(*),ARRAY(*)
      LOGICAL REALSP, FOURSP
      SAVE    REALSP, FOURSP
C
C.....FILES
      COMMON /FILES/INPUT,IOUT,IN290,IN213,ISTORE,IUNIT7,IUNIT8,ISTRUC,
     +               IVNLKK,ISUMRY,IKPTS
C
C-----------------------------------------------------------------------
C
C     WRITE THE RECORD-TYPE
C
      WRITE (IUNIT7,*) 'ATOMWR: Writing record type ', IRECTP
      WRITE (IFILE) IRECTP
C
      IF (IRECTP .EQ. 20) THEN
C
        WRITE (IFILE) NAME,ICORR,IREL,IFCORE,ZION
        WRITE (IUNIT7,100) IFILE,NAME,ICORR,IREL,IFCORE,ZION
100     FORMAT ('0Writing unit=',I3,' with ',A,' atom'/
     +    ' corr=',A,' rel=',A,' core=',A,' ionic charge=',F10.3)
        WRITE (IFILE) NTITLE
        DO 120 I = 1, NTITLE
          WRITE (IFILE) TITLE(I)
          WRITE (IUNIT7,110) TITLE(I)
110       FORMAT(1X,A)
120       CONTINUE
C
      ELSE IF (IRECTP .EQ. 22) THEN
C
        WRITE (IFILE) NR,A,B
        REALSP = .TRUE.
        FOURSP = .FALSE.
        WRITE (IFILE) (R(I), I = 1, NR)
C
      ELSE IF (IRECTP .EQ. 23) THEN
C
        WRITE (IFILE) NQL,DELQL,NQNL,DELQNL
        FOURSP = .TRUE.
        REALSP = .FALSE.
C
      ELSE IF (IRECTP .GE. 31 .AND. IRECTP .LE. 34) THEN
C
        NARRAY = 0
        IF (FOURSP) NARRAY = NQL
        IF (REALSP) NARRAY = NR
        WRITE (IFILE) ICALC,ITYPE,NO,LO,MODE
        WRITE (IFILE) (ARRAY(I), I = 1, NARRAY)
C
      ELSE IF (IRECTP .EQ. 36) THEN
C
        WRITE (IFILE) ICALC,ITYPE,NO,LO,MODE
C
      ELSE
C
        WRITE (IOUT,1000) IRECTP
1000    FORMAT('0Subroutine ATOMRD *** WARNING ***'/
     +    ' illegal record-type =',I10)
C
        ENDIF
C
      RETURN
      END
