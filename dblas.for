             GO TO 70
  150             CONTINUE
             DD1=DD1/GAM**2
             DX1=DX1*GAM
             DH11=DH11*GAM
             DH12=DH12*GAM
        GO TO 140
  160 CONTINUE
  170        CONTINUE
        IF(.NOT. DABS(DD2) .LE. RGAMSQ) GO TO 190
             IF(DD2 .EQ. ZERO) GO TO 220
             ASSIGN 180 TO IGO
C             FIX-H..
             GO TO 70
  180             CONTINUE
             DD2=DD2*GAM**2
             DH21=DH21/GAM
             DH22=DH22/GAM
        GO TO 170
  190 CONTINUE
  200        CONTINUE
        IF(.NOT. DABS(DD2) .GE. GAMSQ) GO TO 220
             ASSIGN 210 TO IGO
C             FIX-H..
             GO TO 70
  210             CONTINUE
             DD2=DD2/GAM**2
             DH21=DH21*GAM
             DH22=DH22*GAM
        GO TO 200
  220 CONTINUE
        IF(DFLAG)250,230,240
  230        CONTINUE
             DPARAM(3)=DH21
             DPARAM(4)=DH12
             GO TO 260
  240        CONTINUE
             DPARAM(2)=DH11
             DPARAM(5)=DH22
             GO TO 260
  250        CONTINUE
             DPARAM(2)=DH11
             DPARAM(3)=DH21
             DPARAM(4)=DH12
             DPARAM(5)=DH22
  260 CONTINUE
        DPARAM(1)=DFLAG
        RETURN
      END
      SUBROUTINE DSCAL(N,DA,DX,INCX)
C
C     REPLACE DOUBLE PRECISION DX BY DOUBLE PRECISION DA*DX.
C     FOR I = 0 TO N-1, REPLACE DX(1+I*INCX) WITH  DA * DX(1+I*INCX)
C
      DOUBLE PRECISION DA,DX(*)
      IF(N.LE.0)RETURN
      IF(INCX.EQ.1)GOTO 20
C
C       CODE FOR INCREMENTS NOT EQUAL TO 1.
C
      NS = N*INCX
        DO 10 I = 1,NS,INCX
        DX(I) = DA*DX(I)
   10        CONTINUE
      RETURN
C
C       CODE FOR INCREMENTS EQUAL TO 1.
C
C
C       CLEAN-UP LOOP SO REMAINING VECTOR LENGTH IS A MULTIPLE OF 5.
C
   20 M = MOD(N,5)
      IF( M .EQ. 0 ) GO TO 40
      DO 30 I = 1,M
      DX(I) = DA*DX(I)
   30 CONTINUE
      IF( N .LT. 5 ) RETURN
   40 MP1 = M + 1
      DO 50 I = MP1,N,5
      DX(I) = DA*DX(I)
      DX(I + 1) = DA*DX(I + 1)
      DX(I + 2) = DA*DX(I + 2)
      DX(I + 3) = DA*DX(I + 3)
      DX(I + 4) = DA*DX(I + 4)
   50 CONTINUE
      RETURN
      END
      DOUBLE PRECISION FUNCTION DSDOT(N,SX,INCX,SY,INCY)
C
C     RETURNS D.P. DOT PRODUCT ACCUMULATED IN D.P., FOR S.P. SX AND SY
C     DSDOT = SUM FOR I = 0 TO N-1 OF  SX(LX+I*INCX) * SY(LY+I*INCY),
C     WHERE LX = 1 IF INCX .GE. 0, ELSE LX = (-INCX)*N, AND LY IS
C     DEFINED IN A SIMILAR WAY USING INCY.
C
      REAL SX(*),SY(*)
C
      DSDOT = 0.D0
      IF(N .LE. 0)RETURN
      IF(INCX.EQ.INCY.AND.INCX.GT.0) GO TO 20
      KX = 1
      KY = 1
      IF(INCX.LT.0) KX = 1+(1-N)*INCX
      IF(INCY.LT.0) KY = 1+(1-N)*INCY
        DO 10 I = 1,N
        DSDOT = DSDOT + DBLE(SX(KX))*DBLE(SY(KY))
        KX = KX + INCX
        KY = KY + INCY
   10 CONTINUE
      RETURN
   20 CONTINUE
      NS = N*INCX
        DO 30 I=1,NS,INCX
        DSDOT = DSDOT + DBLE(SX(I))*DBLE(SY(I))
   30        CONTINUE
      RETURN
      END
      SUBROUTINE DSWAP(N,DX,INCX,DY,INCY)
C
C     INTERCHANGE DOUBLE PRECISION DX AND DOUBLE PRECISION DY.
C     FOR I = 0 TO N-1, INTERCHANGE  DX(LX+I*INCX) AND DY(LY+I*INCY),
C     WHERE LX = 1 IF INCX .GE. 0, ELSE LX = (-INCX)*N, AND LY IS
C     DEFINED IN A SIMILAR WAY USING INCY.
C
      DOUBLE PRECISION DX(1),DY(1),DTEMP1,DTEMP2,DTEMP3
      IF(N.LE.0)RETURN
      IF(INCX.EQ.INCY) IF(INCX-1) 5,20,60
    5 CONTINUE
C
C      CODE FOR UNEQUAL OR NONPOSITIVE INCREMENTS.
C
      IX = 1
      IY = 1
      IF(INCX.LT.0)IX = (-N+1)*INCX + 1
      IF(INCY.LT.0)IY = (-N+1)*INCY + 1
      DO 10 I = 1,N
      DTEMP1 = DX(IX)
      DX(IX) = DY(IY)
      DY(IY) = DTEMP1
      IX = IX + INCX
      IY = IY + INCY
   10 CONTINUE
      RETURN
C
C      CODE FOR BOTH INCREMENTS EQUAL TO 1
C
C
C      CLEAN-UP LOOP SO REMAINING VECTOR LENGTH IS A MULTIPLE OF 3.
C
   20 M = MOD(N,3)
      IF( M .EQ. 0 ) GO TO 40
      DO 30 I = 1,M
      DTEMP1 = DX(I)
      DX(I) = DY(I)
      DY(I) = DTEMP1
   30 CONTINUE
      IF( N .LT. 3 ) RETURN
   40 MP1 = M + 1
      DO 50 I = MP1,N,3
      DTEMP1 = DX(I)
      DTEMP2 = DX(I+1)
      DTEMP3 = DX(I+2)
      DX(I) = DY(I)
      DX(I+1) = DY(I+1)
      DX(I+2) = DY(I+2)
      DY(I) = DTEMP1
      DY(I+1) = DTEMP2
      DY(I+2) = DTEMP3
   50 CONTINUE
      RETURN
   60 CONTINUE
C
C     CODE FOR EQUAL, POSITIVE, NONUNIT INCREMENTS.
C
      NS = N*INCX
      DO 70 I=1,NS,INCX
      DTEMP1 = DX(I)
      DX(I) = DY(I)
      DY(I) = DTEMP1
   70      CONTINUE
      RETURN
      END
      INTEGER FUNCTION IDAMAX(N,DX,INCX)
C
C     FIND SMALLEST INDEX OF MAXIMUM MAGNITUDE OF DOUBLE PRECISION DX.
C     IDAMAX =      FIRST I, I = 1 TO N, TO MINIMIZE  ABS(DX(1-INCX+I*INCX))
C
      DOUBLE PRECISION DX(*),DMAX,XMAG
      IDAMAX = 0
      IF(N.LE.0) RETURN
      IDAMAX = 1
      IF(N.LE.1)RETURN
      IF(INCX.EQ.1)GOTO 20
C
C       CODE FOR INCREMENTS NOT EQUAL TO 1.
C
      DMAX = DABS(DX(1))
      NS = N*INCX
      II = 1
        DO 10 I = 1,NS,INCX
        XMAG = DABS(DX(I))
        IF(XMAG.LE.DMAX) GO TO 5
        IDAMAX = II
        DMAX = XMAG
    5        II = II + 1
   10        CONTINUE
      RETURN
C
C       CODE FOR INCREMENTS EQUAL TO 1.
C
   20 DMAX = DABS(DX(1))
      DO 30 I = 2,N
        XMAG = DABS(DX(I))
        IF(XMAG.LE.DMAX) GO TO 30
        IDAMAX = I
        DMAX = XMAG
   30 CONTINUE
      RETURN
      END
